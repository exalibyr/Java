package com.company;

public class WorkingTime {
    private int beginning;
    private int end;

    public WorkingTime(int beginning, int end) {
        this.beginning = beginning;
        this.end = end;
    }

    public int getBeginning() {
        return beginning;
    }

    public void setBeginning(int beginning) {
        this.beginning = beginning;
    }

    public int getEnd() {
        return end;
    }

    public void setEnd(int end) {
        this.end = end;
    }
}
